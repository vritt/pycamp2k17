#!/usr/bin/python3.4
# String
name = "John"

# Number
number = 100

# Float
floating = 1000.0

# List
mylist = [1, 2, 3]

# Tuple
myTuple = (1, 2, 3)

# Dictionary
myDict = {'one': 1, 'two': 2, 'three': 3}

# Bool
isPrime = True

# None
data = None

print(type(name), name)
print(type(number), number)
print(type(floating), floating)
print(type(mylist), mylist)
print(type(myTuple), mylist)
print(type(myDict), mylist)
print(type(isPrime), isPrime)
print(type(data), data)
